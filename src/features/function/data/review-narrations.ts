  proportional:
    '正比例函数。公式是y等于kx，k不等于零。它的图像是一条过原点的直线。k大于零，直线往右上走；k小于零，直线往右下走。k的绝对值越大，直线越陡。最简单的例子：速度乘以时间等于路程，如果速度是2，那y等于2x。给x等于3，y就是6。',

  linear:
    '一次函数。公式是y等于kx加b，k不等于零。跟正比例函数的区别就是多了一个b。b就是直线跟y轴的交点。如果b等于零，一次函数就变成了正比例函数。生活中出租车计费就是一次函数：起步价5块加上每公里3块，y等于3x加5。',

  inverse:
    '反比例函数。公式是y等于k除以x，k不等于零，x也不等于零。它的图像是两条曲线，叫双曲线。k大于零，曲线在一三象限；k小于零，在二四象限。一个重要特点：x和y的乘积永远等于k。比如6个苹果分给x个人，每人分到y等于6除以x个。',

  quadratic:
    '二次函数。公式是y等于ax平方加bx加c，a不等于零。图像是一条抛物线。a大于零开口朝上，像个碗；a小于零开口朝下，像个伞。顶点是最高点或最低点。最简单的例子是y等于x平方，x等于负2和x等于2的时候，y都等于4，所以图像关于y轴对称。',
